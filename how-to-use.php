<h4><strong>How to use Web Based DAAL</strong></h4>
<ul>
	<li>You may "exclude/keep only" any number from the rankings by hovering over the entry and clicking either "Exclude" or "Keep only"</li>
	<li>You may see the underlying data behind each pivot/ranking by hovering over the number and clicking the spreadsheet icon. On the pop-up, select the underlying tab and check the "Show all columns" checkbox.</li>
	<li>It is possible to download the records of a particular pivot/ranking in .txt format for further analysis.</li>
</ul>

